    private void findUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findUserButtonActionPerformed

        if(delUsername2.getText().equals("Username"))
            JOptionPane.showMessageDialog(null, "Please enter a username.");
        else{

            ArrayList<Member> member = currentMember.getUserList(delUsername2.getText(), 2);
            DefaultTableModel model = (DefaultTableModel) userTable2.getModel();
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            Object[] row = new Object[4];

            for(int i = 0; i < member.size(); i++){
                row[0] = member.get(i).getuName();
                row[1] = member.get(i).getfName();
                row[2] = member.get(i).getlName();
                row[3] = member.get(i).getPassword();

                model.addRow(row);
            }
            if(member.isEmpty())
                JOptionPane.showMessageDialog(null, "There is no such person.");

        }
    }//GEN-LAST:event_findUserButtonActionPerformed

