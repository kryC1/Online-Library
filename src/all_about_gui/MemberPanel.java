    public void diplayTable(ArrayList<Book> list, DefaultTableModel model){
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            Object[] row = new Object[5];
            
            
            if(list.size()!=0){
                for(int i = 0; i < list.size(); i++){

                    row[0] = list.get(i).getTitle();
                    row[1] = list.get(i).getAuthor();
                    row[2] = list.get(i).getPublisher();
                    row[3] = list.get(i).getCategory();
                    row[4] = list.get(i).getScore();
                    model.addRow(row);
                }
            }
            else{
                System.out.println("List is empty.");
                jLabelListName.setText("This list is empty.");
            }
    }

