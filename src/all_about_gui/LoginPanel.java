    private void aLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aLoginButtonActionPerformed
        currentUserName = uNameLog.getText();
        String password = String.valueOf(passLog.getPassword());
        Admin temp = new Admin(currentUserName, password);
        
        
        if(temp.checkUsername(currentUserName)){
            try {
                if(MyConnection.userLogin("admin", currentUserName, password)){
                    AdminPanel ap = new AdminPanel();
                    ap.userName = currentUserName;
                    ap.setVisible(true);
                    ap.pack();
                    ap.setLocationRelativeTo(null);
                    ap.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(LoginPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        else
            JOptionPane.showMessageDialog(null, "Invalid username");
        
        
    }//GEN-LAST:event_aLoginButtonActionPerformed

