    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        try {
            currentUserName = uNameLog.getText();
            String password = String.valueOf(passLog.getPassword());
            Member temp = new Member(currentUserName, "", "", password);
            
            
            if(temp.checkUsername(currentUserName)){
                if(MyConnection.userLogin("member", currentUserName, password)){
                    
                    MemberPanel mp = new MemberPanel();
                    
                    mp.userName = currentUserName;
                    mp.memberlist = temp.getUserList(currentUserName, 2);
                    mp.currentMember = new Member(mp.memberlist.get(0).getuName(), mp.memberlist.get(0).getfName(), mp.memberlist.get(0).getlName(), mp.memberlist.get(0).getPassword());
                    mp.currentMember.getAllLists_from_db(); //listeler db'den yüklenir
                    
                    mp.setVisible(true);
                    mp.pack();
                    mp.setLocationRelativeTo(null);
                    mp.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                }
                
            }
            else                     
                JOptionPane.showMessageDialog(null, "Invalid username.");
        } catch (SQLException ex) {
            Logger.getLogger(LoginPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

