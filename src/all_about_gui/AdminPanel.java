    private void findUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findUserButtonActionPerformed
        try {
            String selectedValue = userType.getSelectedItem().toString();
            Member temp1 = new Member("", "", "", "");
            Admin temp2 = new Admin("", "");

            if(selectedValue.equals("Usertype"))
                JOptionPane.showMessageDialog(null, "Please select a usertype.");
            else if (selectedValue.equals("Member")){
                ArrayList<Member> list = temp1.getUserList(opUsername.getText(), 2);
                DefaultTableModel model = (DefaultTableModel) userTable.getModel();
                model.getDataVector().removeAllElements();
                model.fireTableDataChanged();
                Object[] row = new Object[4];

                for(int i = 0; i < list.size(); i++){
                    row[0] = list.get(i).getuName();
                    row[1] = list.get(i).getfName();
                    row[2] = list.get(i).getlName();
                    row[3] = list.get(i).getPassword();

                    model.addRow(row);
                }
            }
            else if(selectedValue.equals("Admin")){
                ArrayList<Admin> list = temp2.getUserList(opUsername.getText(), 2);
                DefaultTableModel model = (DefaultTableModel) userTable.getModel();
                model.getDataVector().removeAllElements();
                model.fireTableDataChanged();
                Object[] row = new Object[4];

                for(int i = 0; i < list.size(); i++){
                    row[0] = list.get(i).getuName();
                    //row[1] = list.get(i).getfName();
                    //row[2] = list.get(i).getlName();
                    row[3] = list.get(i).getPassword();

                    model.addRow(row);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_findUserButtonActionPerformed

