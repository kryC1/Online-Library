    private void bookFindButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookFindButtonActionPerformed
        ArrayList<Book> list = Book.bookList(bookName.getText(), 2);
        DefaultTableModel model = (DefaultTableModel) bookTable.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        Object[] row = new Object[6];

        for(int i = 0; i < list.size(); i++){
            row[0] = list.get(i).getID();
            row[1] = list.get(i).getTitle();
            row[2] = list.get(i).getAuthor();
            row[3] = list.get(i).getPublisher();
            row[4] = list.get(i).getCategory();
            row[5] = list.get(i).getScore();

            model.addRow(row);
        }
        if(list.isEmpty())
            JOptionPane.showMessageDialog(null, "There is no such book in the system.");
    }//GEN-LAST:event_bookFindButtonActionPerformed

